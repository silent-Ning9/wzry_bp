def load_existing_heroes() -> List[Dict]:
    """加载现有英雄数据"""
    if os.path.exists(HEROES_FILE):
        with open(HEROES_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    return []


def update_hero_lanes(heroes: List[Dict]) -> List[Dict]:
    """更新英雄分路数据"""
    updated = 0
    for hero in heroes:
        name = hero.get('name', '')
        if name in KPL_HERO_LANES:
            old_lanes = hero.get('lanes', [])
            new_lanes = KPL_HERO_LANES[name]
            if old_lanes != new_lanes:
                hero['lanes'] = new_lanes
                updated += 1
                print(f"更新 {name}: {old_lanes} -> {new_lanes}")
        else:
            # 如果没有在KPL数据中，保持原样或使用默认推断
            print(f"未找到 {name} 的KPL分路数据，保持原样")

    print(f"\n共更新 {updated} 个英雄的分路数据")
    return heroes


def save_heroes(heroes: List[Dict]):
    """保存英雄数据"""
    with open(HEROES_FILE, 'w', encoding='utf-8') as f:
        json.dump(heroes, f, ensure_ascii=False, indent=2)
    print(f"已保存 {len(heroes)} 个英雄数据到 {HEROES_FILE}")


def fetch_kpl_online_data():
    """尝试从KPL官网获取在线数据"""
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
        'Referer': 'https://kpl.qq.com/',
        'Accept': 'application/json',
    }

    # 尝试获取KPL数据
    urls = [
        'https://kpl.qq.com/web/data/hero_rank.json',
        'https://kpl.qq.com/web/data/hero_stat.json',
    ]

    for url in urls:
        try:
            resp = requests.get(url, headers=headers, timeout=10)
            if resp.status_code == 200:
                return resp.json()
        except:
            pass

    return None


def update_from_kpl():
    """主函数：更新英雄分路数据"""
    print("=== KPL英雄分路数据更新 ===\n")

    # 加载现有数据
    heroes = load_existing_heroes()
    if not heroes:
        print("未找到现有英雄数据")
        return False

    print(f"加载了 {len(heroes)} 个英雄数据\n")

    # 尝试从KPL获取在线数据
    online_data = fetch_kpl_online_data()
    if online_data:
        print("成功获取KPL在线数据")
        # 这里可以处理在线数据
    else:
        print("无法获取KPL在线数据，使用本地整理的数据")

    # 更新分路
    heroes = update_hero_lanes(heroes)

    # 保存数据
    save_heroes(heroes)

    return True


if __name__ == '__main__':
    update_from_kpl()
